int** initWorld() {
    int **newWorld = (int**)malloc(WORLD_HEIGHT * sizeof(int *));
    for (int y = 0; y < WORLD_HEIGHT; y++) {
        newWorld[y] = (int *)malloc(WORLD_WIDTH * sizeof(int));
    }
    return newWorld;
}

void copyWorldFromMatrix(int **world, int worldMatrix[32][64]) {
    for (int y = 0; y < WORLD_HEIGHT; y++) {
        for (int x = 0; x < WORLD_WIDTH; x++) {
            world[y][x] = worldMatrix[y][x];
        }
    }
}

int* getNeighbours(int **world, int x, int y, int *neighbours) {
    int currNeighbour = 0;
    int xplus1 = (x + 1) % WORLD_WIDTH;
    int xminus1 = x - 1;
    if (xminus1 < 0) {
        xminus1 += WORLD_WIDTH;
    }
    int yplus1 = (y + 1) % WORLD_HEIGHT;
    int yminus1 = y - 1;
    if (yminus1 < 0) {
        yminus1 += WORLD_HEIGHT;
    }
    neighbours[currNeighbour++] = world[y][xminus1];
    neighbours[currNeighbour++] = world[y][xplus1];
    neighbours[currNeighbour++] = world[yminus1][x];
    neighbours[currNeighbour++] = world[yplus1][x];
    neighbours[currNeighbour++] = world[yminus1][xminus1];
    neighbours[currNeighbour++] = world[yminus1][xplus1];
    neighbours[currNeighbour++] = world[yplus1][xminus1];
    neighbours[currNeighbour++] = world[yplus1][xplus1];
}

int generateCellValue(int **world, int x, int y) {
    int *neighbours = (int *)malloc(sizeof(int) * NUM_NEIGHBOURS);
    getNeighbours(world, x, y, neighbours);
    int liveNeighbours = 0;
    int deadNeighbours = 0;

    for (int i = 0; i < 8; i++) {
        if (neighbours[i] == 1) {
            liveNeighbours++;
        } else {
            deadNeighbours++;
        }
    }
    int cellValue = 0;

    if (world[y][x] == 1) {
        if (liveNeighbours < 2 || liveNeighbours > 3) {
            cellValue = 0;
        } else {
            cellValue = 1;
        }
    } else {
        if (liveNeighbours == 3) {
            cellValue = 1;
        } else {
            cellValue = 0;
        }
    }

    return cellValue;
}

int generateWorldFromCurrent(int **world) {
    int **newWorld = initWorld();

    for (int y = 0; y < WORLD_HEIGHT; y++) {
        for (int x = 0; x < WORLD_WIDTH; x++) {
            newWorld[y][x] = generateCellValue(world, x, y);
        }
    }

    for (int y = 0; y < WORLD_HEIGHT; y++) {
        for (int x = 0; x < WORLD_WIDTH; x++) {
            world[y][x] = newWorld[y][x];
        }
    }

    for(int y = 0; y < WORLD_HEIGHT; y++) {
        free(newWorld[y]);
    }
    free(newWorld);

}

void render(int screenWidth, int screenHeight, int **world) {
    for (int worldY = 0; worldY < WORLD_HEIGHT; worldY++) {
        for (int worldX = 0; worldX < WORLD_WIDTH; worldX++) {
            int screenX = worldX * SCALE;
            int screenY = worldY * SCALE;
            if (world[worldY][worldX] == 1) {
                DrawRectangle(screenX, screenY, SCALE, SCALE, (Color){0, 0, 0, 255});
            }
        }
    }

    for (int y = 0; y < WORLD_HEIGHT; y++) {
        for (int x = 0; x < WORLD_WIDTH; x++) {
            DrawLine(x * SCALE, 0, x * SCALE, screenHeight, (Color){180, 180, 180, 255});
        }
        DrawLine(0, y * SCALE, screenWidth, y * SCALE, (Color){180, 180, 180, 255});
    }
}

int main()
{
    SetTargetFPS(FPS);
    int screenWidth = WORLD_WIDTH * SCALE;
    int screenHeight = WORLD_HEIGHT * SCALE;

    int **world = initWorld();
    copyWorldFromMatrix(world, currentWorld);

    InitWindow(screenWidth, screenHeight, "Conway's game of life");
    while (!WindowShouldClose())
    {
        
        BeginDrawing();
            ClearBackground(RAYWHITE);
            render(screenWidth, screenHeight, world);
        EndDrawing();
        generateWorldFromCurrent(world);
    }

    return 0;
}